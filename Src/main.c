void blink(uint8_t byte) {
    uint8_t i;
    for (i = 0; i < 8; ++i) {
        if ((byte >> i) & 1) {
            errf_set();
        } else {
            errf_reset();
        }
        HAL_Delay(100);
    }
    errf_reset();
}

uint32_t u32_load(const uint8_t *data) {
  return (data[0]<<24) | (data[1]<<16) | (data[2]<<8) | data[3];
}

void u32_store(uint8_t *data, uint32_t val) {
  data[0] = (val>>24) & 0xFF;
  data[1] = (val>>16) & 0xFF;
  data[2] = (val>>8) & 0xFF;
  data[3] = val & 0xFF;
}
